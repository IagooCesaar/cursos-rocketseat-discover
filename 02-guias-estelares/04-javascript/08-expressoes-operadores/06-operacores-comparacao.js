// Operadores de comparação

let one = 1; // =    atribuição
let two = 2; // =    atribuição

// ==     igual
console.log("> igual");
console.log(one == one);
console.log(one == two);
console.log(one == 1);
console.log(one == "1");

// !=     diferente
console.log("> diferente");
console.log(one != one);
console.log(one != two);
console.log(one != 1);
console.log(one != "1");

// ===    estritamente igual
console.log("> estritamente igual");
console.log("> igual");
console.log(one === one);
console.log(one === two);
console.log(one === 1);
console.log(one === "1");

// !==    estritamente diferente
console.log("> estritamente diferente");

// >      maior
console.log("> maior");
console.log(one > 1);
console.log(one > two);

// >=     maior ou igual
console.log("> maior ou igual");
console.log(one >= 1);
console.log(one >= two);

// <      menor
console.log("> menor");
console.log(one < 1);
console.log(one < two);

// <=     menor ou igual
console.log("> menor ou igual");
console.log(one <= 1);
console.log(one <= two);
