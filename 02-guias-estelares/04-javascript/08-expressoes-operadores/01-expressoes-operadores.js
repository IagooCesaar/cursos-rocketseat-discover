//Expressão:
let number = 1;

//Operação binária
console.log(number + 1);

//Operação unária
console.log(--number);
console.log(++number);

// Operação ternária
console.log(
  number > 0 ? "Number é maior que zero" : "Number não é maior que zero"
);
