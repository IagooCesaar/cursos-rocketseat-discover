// Operadores de atribuição

let x;

// assingment
x = 1;

// addition assignment
x += 2;
console.log(x);

// substraction assignment
x -= 1;
console.log(x);

// multiplication assignment
x *= 6;
console.log(x);

// division assignment
x /= 4;
console.log(x);

// exponetiation assigment
x **= 2;
console.log(x);

// remainder assingment
x %= 7;
console.log(x);
